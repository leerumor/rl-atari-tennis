def main():
    experiment_dir = "runs/best"

    device = torch.device('cpu')
    env = gym.make("TennisNoFrameskip-v4")
    env = wrap_atari(env)
    env = gym.wrappers.RecordEpisodeStatistics(env) # records episode reward in `info['episode']['r']`
    env = wrap_deepmind(env,clip_rewards=True,frame_stack=True,scale=False)

    q_network = QNetwork(env).to(device)
    q_network.load_state_dict(torch.load(os.path.join(experiment_dir, "model-9100000.pth"), map_location='cpu'))

    # TRY NOT TO MODIFY: start the game
    state = env.reset()
    episode_rewards = []
    episode_reward = 0
    simple_image_viewer = SimpleImageViewer()
    # Test performance over several episodes
    while True:

        state = np.array(state)
        logits = q_network.forward(state.reshape((1,)+state.shape), device)
        action = torch.argmax(logits, dim=1).tolist()[0]

        state, reward, done, info = env.step(action)  # Step
        episode_reward += reward
        rendered = env.render()
        rendered = cv2.resize(rendered, (rendered.shape[1]*6, rendered.shape[0]*6), interpolation=cv2.INTER_AREA)
        if isinstance(rendered, np.ndarray) and len(rendered.shape) == 3:
            if simple_image_viewer:
                simple_image_viewer.imshow(rendered)
        time.sleep(0.02)

        if done:
            if "TimeLimit.truncated" in info and info["TimeLimit.truncated"]:
                episode_reward = -24
            episode_rewards.append(episode_reward)
            state, episode_reward, done = env.reset(), 0, False

    env.close()


